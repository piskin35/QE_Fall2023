package day20_for_loops;

public class C03_Nested_For_loops {

    /*

    	* * * * * * * *
    	* * * * * * * *
     	* * * * * * * *
     	* * * * * * * *

     */

    public static void main(String[] args) {

        for (int i=1; i<=10; i++){

            for(int j=1; j<=10; j++){

                System.out.print("* ");

            }

            System.out.println();

        }

    }
}
